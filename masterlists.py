def extraer_canales(servidores):
    lista_candidatos = []
    print("🕵️ Buscando canales en los servidores...")

    for url in servidores:
        try:
            # El robot toca la puerta del servidor
            respuesta = requests.get(url, timeout=15)
            lineas = respuesta.text.split('\n')

            canal_actual = None
            for i in range(len(lineas)):
                linea = lineas[i].strip()

                # Si la línea empieza con #EXTINF, ahí está el nombre
                if linea.upper().startswith("#EXTINF"):
                    # Extraemos el nombre que viene después de la última coma
                    nombre = linea.split(',')[-1].strip()
                    # La dirección (URL) suele estar en la siguiente línea
                    if i + 1 < len(lineas):
                        url_canal = lineas[i+1].strip()
                        if url_canal.startswith("http"):
                            lista_candidatos.append({
                                "nombre_original": nombre,
                                "url": url_canal
                            })
            print(f"✅ Leídos los canales de: {url}")
        except:
            print(f"❌ No pude entrar al servidor: {url}")

    return lista_candidatos

