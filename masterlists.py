import time

def inspeccionar_y_limpiar(canales, nombres_maestros):
    validos = []
    invalidos = []
    total = len(canales)

    # Cabeceras para que el servidor no nos bloquee
    headers = {'User-Agent': 'VLC/3.0.18'}

    print(f"🚀 Iniciando inspección de {total} canales...")

    for i, canal in enumerate(canales):
        nombre_original = canal['nombre_original']
        url = canal['url']

        # 1. EL EXAMEN (¿Funciona?)
        try:
            # Usamos stream=True para no descargar el video entero, solo ver si abre
            with requests.get(url, headers=headers, timeout=5, stream=True) as r:
                if r.status_code == 200:
                    estado = "✅ OK"

                    # 2. HOMOGENIZAR (Buscar nombre bonito)
                    # Comparamos el nombre original con los 26,000 nombres maestros
                    mejor_coincidencia, puntaje = process.extractOne(
                        nombre_original,
                        nombres_maestros,
                        scorer=fuzz.token_sort_ratio
                    )

                    # Si el parecido es mayor al 80%, usamos el nombre maestro
                    nombre_final = mejor_coincidencia if puntaje > 80 else nombre_original

                    validos.append({"nombre": nombre_final, "url": url})
                else:
                    estado = "❌ FAIL (Status)"
                    invalidos.append(canal)
        except:
            estado = "❌ FAIL (Timeout/Error)"
            invalidos.append(canal)

        # Reporte de progreso cada 10 canales para no llenar la pantalla
        if (i + 1) % 10 == 0 or i + 1 == total:
            print(f"Progreso: [{i + 1}/{total}] - {nombre_original} -> {estado}")

        # 3. PAUSA DE CORTESÍA (Para no ser bloqueados)
        time.sleep(0.5)

    return validos, invalidos

